    `checkout_id`,
    `checkout_token`,
    `app_id`,
    `browser_ip`,
    `customer_locale`,
    `note`,
    `note_attributes`,
    `order_status_url`,
    `payment_gateway_names`,
    `tags`,
    `test`,
    `total_weight`,
    `user_id`,
    TO_TIMESTAMP(`created_at`),
    TO_TIMESTAMP(`updated_at`),
